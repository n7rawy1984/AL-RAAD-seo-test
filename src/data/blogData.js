function toIsoDate(arabicDate) {
  if (!arabicDate) return "2026-03-25";
  const parts = arabicDate.trim().split(" ");
  if (parts.length < 3) return "2026-03-25";

  const day = String(parts[0]).padStart(2, "0");
  const month = arabicMonths[parts[1]] || "03";
  const year = parts[2];

  return `${year}-${month}-${day}`;
}

